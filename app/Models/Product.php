 */
class Product extends Model
{
    use HasFactory;

    /**
     * scopeOrderByIdPaginated
     *
     * @param  mixed $query
     * @return Builder
     */
    public function scopeOrderByIdPaginated($query)
    {
        return $query->orderBy('id')->paginate(12);
    }
}
